package com.example.controlspinner

class Operaciones {
    fun sumar(val1: Double, val2: Double): Double{
        return val1 + val2
    }

    fun restar(val1: Double, val2: Double): Double{
        return val1 - val2
    }
    fun multiplicar(val1: Double, val2: Double): Double{
        return val1 * val2
    }
}